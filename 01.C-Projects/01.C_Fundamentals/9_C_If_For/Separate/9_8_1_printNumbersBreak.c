/*
Модифицирайте, така че програмата да пропусне 5 и 7;
Продължение 2 Модифицирайте, така че програмата да спре на 9.
*/

#include <stdio.h>

int main()
{
	for (int i = 1; i <= 10; i++)
	{
		if (i == 5 || i == 7)
		{
			continue;
		}
		if (i == 9)
		{
			break;
		}
		printf("\n%d", i);
	}
	return 0;
}